def would_break_phrase(words, idx):
    """Check if splitting at *idx* (between words[idx-1] and words[idx])
    would break a known non-splittable phrase bigram.

    If the word before the split ends with a sentence-ending period
    (. ! ?), the split is always allowed 
    - it's a genuine sentence boundary, not an inside-phrase split.
    """
    if idx <= 0 or idx >= len(words):
        return False
    if words[idx - 1]['text'].rstrip().endswith(('.', '!', '?')):
        return False  # genuine sentence boundary -> never blocked

    def _clr(w):
        t = w['text'].strip().lower().rstrip('.,!?;:\'"')
        t = re.sub(r"'(s|re|ve|m|ll|d|t)$", '', t)
        return t

    prev_clean = _clr(words[idx - 1])
    curr_clean = _clr(words[idx])
    return (prev_clean, curr_clean) in NON_SPLIT_BIGRAMS
